package com.example.uibeautifulcollection2.item;

public class DashBoardItem {
    private int imageD;
    private String textD;

    public DashBoardItem(int imageD, String textD) {
        this.imageD = imageD;
        this.textD = textD;
    }

    public int getImageD() {
        return imageD;
    }

    public void setImageD(int imageD) {
        this.imageD = imageD;
    }

    public String getTextD() {
        return textD;
    }

    public void setTextD(String textD) {
        this.textD = textD;
    }
}
